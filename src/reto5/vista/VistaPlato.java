    public void mostrarPlatos(){
        PlatoControlador control = new PlatoControlador();
        List<Plato> platos = control.obtenerTodosLosPlatos();
        ModeloTablaPlato modeloTabla = new ModeloTablaPlato(platos);
        JT_Tabla_Platos.setModel(modeloTabla);
        JT_Tabla_Platos.revalidate();
    }
