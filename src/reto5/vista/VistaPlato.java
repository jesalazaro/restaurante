    private void jB_Editar_TbPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_Editar_TbPlatoActionPerformed
        Integer id = (Integer) JT_Tabla_Platos.getModel().getValueAt(JT_Tabla_Platos.getSelectedRow(), 0);
        PlatoControlador control = new PlatoControlador();
        Plato plato = control.obtenerPlatoPorId(id);
        VistaInsertarPlato vInsertarPlato = new VistaInsertarPlato(plato, this);//inicia la instancia vista insertar cliente en caso de modificar un plato
        vInsertarPlato.setVisible(true);
    }//GEN-LAST:event_jB_Editar_TbPlatoActionPerformed

