    private void jB_Editar_TbTipoPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_Editar_TbTipoPlatoActionPerformed
        Integer id = (Integer) JT_Tabla_TipoPlatos.getModel().getValueAt(JT_Tabla_TipoPlatos.getSelectedRow(), 0);
        TipoPlatoControlador control = new TipoPlatoControlador();
        TipoPlato tipoplato = control.obtenerTipoPlatoPorId(id);//inicia la instancia vista insertar cliente en caso de modificar un tipo de plato
        VistaInsertarTipoPlato vInsertarPlato = new VistaInsertarTipoPlato(tipoplato, this);
        vInsertarPlato.setVisible(true);
    }//GEN-LAST:event_jB_Editar_TbTipoPlatoActionPerformed

