    public void mostrarTipoPlatos(){
        TipoPlatoControlador control = new TipoPlatoControlador();
        List<TipoPlato> tipoplatos = control.obtenerTodosLosTipoPlatos();
        ModeloTablaTipoPlato modeloTabla = new ModeloTablaTipoPlato(tipoplatos);
        JT_Tabla_TipoPlatos.setModel(modeloTabla);
        JT_Tabla_TipoPlatos.revalidate();
    }
