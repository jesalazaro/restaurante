    private void jB_Guardar_PlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_Guardar_PlatoActionPerformed
        String PltNombre = jT_Ingresar_PlatoNombre.getText();
        long PltPrecio = Long.valueOf(jT_Ingresar_PlatoPrecio.getText());
        String PltDescripcion = jT_Ingresar_Id_Mesero_Orden.getText();
        int TipoPlatoId = Integer.valueOf(jT_Ingresar_TipoPlatoId.getText());
     
        PlatoControlador control = new PlatoControlador();
        Plato platoLocal = new Plato(PltNombre, PltPrecio, PltDescripcion, TipoPlatoId);
        if(jB_Guardar_Plato.getText().equals("Actualizar")){
            control.actualizarPlato(platoLocal,plato.getPltId());
            vistaPlato.mostrarPlatos();
             JOptionPane.showMessageDialog(this,"Se actualizo un plato");
        }
        else{
            control.insertarPlato(platoLocal);
             JOptionPane.showMessageDialog(this,"Se guardo un plato");
        }
    }//GEN-LAST:event_jB_Guardar_PlatoActionPerformed

