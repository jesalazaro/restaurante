    private void jB_Editar_TbOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_Editar_TbOrdenActionPerformed
        Integer id = (Integer) JT_Tabla_Ordenes.getModel().getValueAt(JT_Tabla_Ordenes.getSelectedRow(), 0);
        OrdenControlador control = new OrdenControlador();
        Orden orden = control.obtenerOrdenPorId(id);
        VistaInsertarOrden vInsertarOrden = new VistaInsertarOrden(orden, this);//inicia la instancia vista insertar cliente en caso de modificar una orden
        vInsertarOrden.setVisible(true);
    }//GEN-LAST:event_jB_Editar_TbOrdenActionPerformed

