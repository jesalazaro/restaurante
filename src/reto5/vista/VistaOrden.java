    public void mostrarOrdenes(){
        OrdenControlador control = new OrdenControlador();
        List<Orden> ordenes = control.obtenerTodasLasOrdenes();
        ModeloTablaOrden modeloTabla = new ModeloTablaOrden(ordenes);
        JT_Tabla_Ordenes.setModel(modeloTabla);
        JT_Tabla_Ordenes.revalidate();
    }
