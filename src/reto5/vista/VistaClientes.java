    public void mostrarClientes(){
        ClienteControlador control = new ClienteControlador();
        List<Cliente> clientes = control.obtenerTodosLosClientes();
        ModeloTablaCliente modeloTabla = new ModeloTablaCliente(clientes); //se adjunta el modelo de tabla para clientes
        TablaClientes.setModel(modeloTabla);
        TablaClientes.revalidate();
    }
