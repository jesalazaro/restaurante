    private void jB_Editar_TbclientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jB_Editar_TbclientesMouseClicked
        Integer id = (Integer) TablaClientes.getModel().getValueAt(TablaClientes.getSelectedRow(), 0);
        ClienteControlador control = new ClienteControlador();
        Cliente cliente = control.obtenerClientePorId(id);
        VistaInsertarCliente vInsertarCliente = new VistaInsertarCliente(cliente, this); //inicia la instancia vista insertar cliente en caso de modificar un cliente
        vInsertarCliente.setVisible(true);
        
    }//GEN-LAST:event_jB_Editar_TbclientesMouseClicked

